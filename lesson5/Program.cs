
// Addition  ("+") :  5 + 3 = 8

int a = 40; 
int b = 30;
int ab = a + b;
Console.WriteLine("The value of ab is " + ab);



// Subtraction  ("-") :  5 - 3 = 2

int c = 40;
int d = 30;

int cd = c - d;
Console.WriteLine("The value of cd is " + cd);




// Multiplication  ("*") :  5 * 3 = 15

int e = 5;
int f = 10;

int ef = e * f;
Console.WriteLine("The value of ef is " + ef);


// Division  ("/") :  5 / 3 = 1


int g = 7;
int h = 3;
int gh = g / h;

Console.WriteLine("The value of gh is " + gh);



// Modulus  ("%") :  5 % 3 = 2

int i = 7;
int j = 3;
int ij = i % j;


// Increment  ("++") :  5++ = 6
// Decrement  ("--") :  5-- = 4


int k = 6;
k++; // increment by 1

Console.WriteLine("The value of k is " + k);

 int KB = 6 ;
 KB -- ; // decrement by 1

Console.WriteLine("The value of KB is " + KB);



//  Relational Operators 

//  Equal to (==) :  5 == 3 = False

int l = 10;
int m = 5;
bool lm = l == m;

Console.WriteLine("The value of lm is " + lm);


//  Not Equal to (!=) :  5 != 3 = True


int n = 10;
int o = 10;

bool no = n != o;

Console.WriteLine("The value of no is " + no);


//  Greater than (>) :  5 > 3 = True

int p = 10;
int q = 5;
bool pq = p > q;
Console.WriteLine("The value of pq is " + pq);


//  Less than (<) :  5 < 3 = False

int r = 10;
int s = 5;

bool rs = r < s;

Console.WriteLine("The value of rs is " + rs);



// Logical Operators

//  AND (&&) :  5 > 3 && 5 < 10 = True

int t = 10;
int u = 5;
int v = 10;
bool tuv = t > u && t < v;
Console.WriteLine("The value of tuv is " + tuv);


// OR (||) :  5 > 3 || 5 < 10 = True

int w = 10;
int x = 5;
int y = 10;

bool wxy = w > x || w < y;
Console.WriteLine("The value of wxy is " + wxy);



// NOT (!) :  !(5 > 3) = False

int z = 10;
int aa = 10;
 bool zaa = !(z > aa);
Console.WriteLine("The value of zaa is " + zaa);


